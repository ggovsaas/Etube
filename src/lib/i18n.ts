// Helper function to get base language from locale (e.g., en-US -> en, es-MX -> es)
function getBaseLanguage(locale: string): string {
  return locale.split('-')[0];
}

// Get translations with smart fallback: locale -> base language -> default
export function getTranslations(locale: Locale) {
  // Try exact locale match
  if (translations[locale]) {
    return translations[locale];
  }
  
  // Try base language (e.g., en-US -> en)
  const baseLang = getBaseLanguage(locale);
  if (baseLang !== locale && translations[baseLang as Locale]) {
    return translations[baseLang as Locale];
  }
  
  // Fallback to default
  return translations[defaultLocale];
}

export function getAlternateUrls(pathname: string, baseUrl: string = process.env.NEXT_PUBLIC_BASE_URL || 'https://yourwebsite.com') {
  const cleanPath = pathname.replace(/^\/(pt|es|pt-AO|nl|es-CO|pt-BR|es-CL|de|nl-BE|fr-BE)/, '') || '/';
  const currentLocale = getLocale(pathname);

  const alternates = locales.map((locale) => ({
    locale,
    url: `${baseUrl}/${locale}${cleanPath === '/' ? '' : cleanPath}`,
  }));

  return {
    current: alternates.find((a) => a.locale === currentLocale)?.url || `${baseUrl}${cleanPath}`,
    alternates,
  };
}


